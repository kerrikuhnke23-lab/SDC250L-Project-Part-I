-- Question 19

SELECT A.TICKETID,
       A.DATEUPDATED,
       B.TICKETID,
       B.DATEUPDATED
FROM USERSUPPORT A
JOIN USERSUPPORT B
ON A.DATEUPDATED = B.DATEUPDATED
AND A.TICKETID < B.TICKETID
WHERE A.STATUS = 'CLOSED'
AND B.STATUS = 'CLOSED';

