-- Question 13:
SELECT AVG(DATEUPDATED - DATESUBMITTED) AS AVG_DAYS,
       MAX(DATEUPDATED - DATESUBMITTED) AS MAX_DAYS,
       MIN(DATEUPDATED - DATESUBMITTED) AS MIN_DAYS
FROM USERSUPPORT
WHERE STATUS = 'CLOSED';

-- Question 14:
