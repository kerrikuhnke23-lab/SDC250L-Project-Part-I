-- Question 3

SELECT U.*,
       CASE
           WHEN FLOOR(MONTHS_BETWEEN(SYSDATE, BIRTHDAY) / 12) >= 18
           THEN 'Adult'
           ELSE 'Minor'
       END AS AGE_GROUP
FROM USERBASE U;

