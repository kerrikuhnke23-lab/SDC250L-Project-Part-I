SELECT U.*,
       CASE
           WHEN STATUS <> 'CLOSED'
            AND DATEUPDATED < SYSDATE - 7
           THEN 'High Priority'
           ELSE ''
       END AS PRIORITY
FROM USERSUPPORT U;

