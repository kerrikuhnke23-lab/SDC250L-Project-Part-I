-- Question 17:
CREATE VIEW DISCOUNTED_PRODUCTS_VIEW AS
SELECT PRODUCTNAME,
       ROUND(PRICE * 0.75, 2) AS DISCOUNTED_PRICE
FROM PRODUCTLIST
WHERE RELEASEDATE <= ADD_MONTHS(SYSDATE, -60);

